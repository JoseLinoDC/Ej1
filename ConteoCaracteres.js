// Función para contar los caracteres de una cadena
function contarCaracteres(cadena) {
    return cadena.length;
}

// Ejemplo de uso
const texto = "Hola, ¿cómo estás?";
const numeroDeCaracteres = contarCaracteres(texto);

console.log("El número de caracteres en la cadena es: " + numeroDeCaracteres);
